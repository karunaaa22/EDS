#Tupple Program

n = (67,78,67,75,84,47)

#printing all element in tupple
for i in n:
    print(i)

#length of tupple
print(f"Length of tupple is: {len(n)}")

#maximum value in tupple
m = max(n)
print(f"Maximum Value of Tupple: {m}")

#Counting occurence of element

print("count of 67 is:", n.count(67))
